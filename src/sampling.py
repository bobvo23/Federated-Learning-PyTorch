def cub_noniid(dataset, num_users):
    """
    Sample non-I.I.D client data from CUB200 dataset
    :param dataset:
    :param num_users:
    :return:
    """
    # 5,994 training imgs --> 10 shards -> 200 classes 30 exaples per class
    num_shards = 200
    num_imgs = len(dataset.imgs) // num_shards
    idx_shard = [i for i in range(num_shards)]
    dict_users = {i: np.array([]) for i in range(num_users)}
    idxs = np.arange(len(dataset.imgs))
    # labels = dataset.train_labels.numpy()
    #labels = np.array(dataset.train_labels)
    labels = np.array(dataset.imgs).transpose((1, 0))[1, :]

    # sort labels
    idxs_labels = np.vstack((idxs, labels))

    # cub200 is pre-sorted
    #idxs_labels = idxs_labels[:, idxs_labels[1, :].argsort()]

    idxs = idxs_labels[0, :]

    remaining_idxs_labels = idxs_labels[num_shards*num_imgs + 1:]
    remaining_idxs = idxs[num_shards*num_imgs:]

    idxs_labels = idxs_labels[:num_shards*num_imgs]
    idxs = idxs[:num_shards*num_imgs]

    # divide and assign
    for i in range(num_users):
        rand_set = set(np.random.choice(idx_shard, 2, replace=False))
        idx_shard = list(set(idx_shard) - rand_set)
        for rand in rand_set:
            dict_users[i] = np.concatenate(
                (dict_users[i], idxs[rand*num_imgs:(rand+1)*num_imgs]), axis=0)

    # Next, assign the remaining data
    for i in range(remaining_idxs.shape[0]):
        user_idx = i % num_users
        dict_users[user_idx] = np.concatenate(
            (dict_users[user_idx], remaining_idxs[i:i+1]), axis=0)

    return dict_users

